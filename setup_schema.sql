-- ====================================================================
DROP TABLE DIAGNOSTIC_LOGS CASCADE CONSTRAINTS PURGE;
DROP TABLE AI_ADVISORIES CASCADE CONSTRAINTS PURGE;
DROP TABLE FINDING_REVIEWS CASCADE CONSTRAINTS PURGE;
DROP TABLE VALIDATION_FINDINGS CASCADE CONSTRAINTS PURGE;
DROP TABLE VALIDATION_RULES CASCADE CONSTRAINTS PURGE;
DROP TABLE BOM_RUNS CASCADE CONSTRAINTS PURGE;
DROP TABLE BOM_COMPONENTS CASCADE CONSTRAINTS PURGE;
DROP TABLE BOMS CASCADE CONSTRAINTS PURGE;


-- --------------------------------------------------------------------
-- 1. CREATE TABLES
-- --------------------------------------------------------------------

-- 1.1 BOMS Table
CREATE TABLE BOMS (
    BOM_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BILL_SEQUENCE_ID VARCHAR2(200 CHAR),
    ORGANIZATION_CODE VARCHAR2(100 CHAR) NOT NULL,
    ITEM_NUMBER VARCHAR2(200 CHAR) NOT NULL,
    STRUCTURE_NAME VARCHAR2(200 CHAR) NOT NULL,
    DESCRIPTION VARCHAR2(1000 CHAR),
    EFFECTIVITY_CONTROL VARCHAR2(100 CHAR),
    SOURCE_UPDATED_AT TIMESTAMP(6) WITH TIME ZONE,
    IMPORT_BATCH_ID VARCHAR2(100 CHAR),
    IMPORTED_AT TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    ITEM_CLASS VARCHAR2(200 CHAR),
    HEALTH_SCORE NUMBER(5,2) NOT NULL CHECK (HEALTH_SCORE BETWEEN 0 AND 100),
    STATUS_LABEL VARCHAR2(30 CHAR) NOT NULL CHECK (STATUS_LABEL IN ('HEALTHY', 'RISKY')),
    CONSTRAINT boms_uq UNIQUE (ORGANIZATION_CODE, ITEM_NUMBER, STRUCTURE_NAME)
);

-- 1.2 BOM_COMPONENTS Table
CREATE TABLE BOM_COMPONENTS (
    BOM_COMPONENT_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BOM_ID NUMBER(19) NOT NULL,
    COMPONENT_SEQUENCE_ID VARCHAR2(200 CHAR),
    PARENT_ITEM_NUMBER VARCHAR2(200 CHAR) NOT NULL,
    COMPONENT_ITEM_NUMBER VARCHAR2(200 CHAR) NOT NULL,
    COMPONENT_ITEM_CLASS VARCHAR2(200 CHAR),
    QUANTITY NUMBER(28,10),
    UOM_CODE VARCHAR2(50 CHAR),
    ITEM_SEQUENCE_NUMBER NUMBER(10),
    OPERATION_SEQUENCE VARCHAR2(100 CHAR),
    ITEM_STATUS VARCHAR2(100 CHAR),
    EFFECTIVITY_START TIMESTAMP(6) WITH TIME ZONE,
    EFFECTIVITY_END TIMESTAMP(6) WITH TIME ZONE,
    BOM_LEVEL NUMBER(10) NOT NULL CHECK (BOM_LEVEL >= 1),
    COMPONENT_PATH VARCHAR2(4000 CHAR),
    ANOMALY_MIN_QUANTITY NUMBER(28,10),
    ANOMALY_MAX_QUANTITY NUMBER(28,10),
    IMPORTED_AT TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    CONSTRAINT bom_comp_fk FOREIGN KEY (BOM_ID) REFERENCES BOMS (BOM_ID) ON DELETE CASCADE
);

-- 1.3 BOM_RUNS Table
CREATE TABLE BOM_RUNS (
    RUN_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BOM_ID NUMBER(19) NOT NULL,
    RUN_KIND VARCHAR2(30 CHAR) NOT NULL CHECK (RUN_KIND IN ('IMPORT', 'REFRESH', 'VALIDATION', 'ADVISORY_AI')),
    TRIGGER_TYPE VARCHAR2(30 CHAR) NOT NULL CHECK (TRIGGER_TYPE IN ('OIC', 'UI_REFRESH', 'ON_DEMAND', 'DAILY', 'SCHEDULER', 'USER_AI')),
    STATUS VARCHAR2(30 CHAR) NOT NULL,
    SOURCE_MODE VARCHAR2(30 CHAR) CHECK (SOURCE_MODE IN ('LIVE', 'MOCK', 'N/A')),
    IDEMPOTENCY_KEY VARCHAR2(200 CHAR),
    CORRELATION_ID VARCHAR2(100 CHAR) NOT NULL UNIQUE,
    REQUESTED_BY VARCHAR2(200 CHAR),
    STARTED_AT TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    COMPLETED_AT TIMESTAMP(6) WITH TIME ZONE,
    INPUT_COUNT NUMBER(19) NOT NULL CHECK (INPUT_COUNT >= 0),
    FINDING_COUNT NUMBER(19) NOT NULL CHECK (FINDING_COUNT >= 0),
    HEALTH_SCORE NUMBER(5,2) CHECK (HEALTH_SCORE BETWEEN 0 AND 100),
    ERROR_CODE VARCHAR2(100 CHAR),
    ERROR_MESSAGE VARCHAR2(4000 CHAR),
    CONSTRAINT bom_runs_fk FOREIGN KEY (BOM_ID) REFERENCES BOMS (BOM_ID) ON DELETE CASCADE
);

-- 1.4 VALIDATION_RULES Table
CREATE TABLE VALIDATION_RULES (
    RULE_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RULE_CODE VARCHAR2(50 CHAR) NOT NULL UNIQUE,
    RULE_NAME VARCHAR2(200 CHAR) NOT NULL,
    SEVERITY VARCHAR2(20 CHAR) NOT NULL CHECK (SEVERITY IN ('CRITICAL', 'HIGH', 'WARNING', 'INFO')),
    DESCRIPTION VARCHAR2(1000 CHAR) NOT NULL,
    THRESHOLD_OR_CONFIGURATION VARCHAR2(1000 CHAR),
    ENABLED_FLAG CHAR(1) NOT NULL CHECK (ENABLED_FLAG IN ('Y', 'N')),
    CREATED_AT TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    UPDATED_AT TIMESTAMP(6) WITH TIME ZONE
);

-- 1.5 VALIDATION_FINDINGS Table
CREATE TABLE VALIDATION_FINDINGS (
    FINDING_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RUN_ID NUMBER(19) NOT NULL,
    BOM_COMPONENT_ID NUMBER(19),
    RULE_ID NUMBER(19) NOT NULL,
    FINDING_KEY VARCHAR2(400 CHAR) NOT NULL,
    ISSUE_STATUS VARCHAR2(20 CHAR) NOT NULL CHECK (ISSUE_STATUS IN ('OPEN', 'REVIEWED', 'IGNORED', 'RESOLVED')),
    ACTUAL_VALUE VARCHAR2(1000 CHAR),
    EXPECTED_VALUE VARCHAR2(1000 CHAR),
    EVIDENCE_JSON CLOB NOT NULL CHECK (EVIDENCE_JSON IS JSON),
    CREATED_AT TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    CONSTRAINT vf_run_fk FOREIGN KEY (RUN_ID) REFERENCES BOM_RUNS (RUN_ID) ON DELETE CASCADE,
    CONSTRAINT vf_comp_fk FOREIGN KEY (BOM_COMPONENT_ID) REFERENCES BOM_COMPONENTS (BOM_COMPONENT_ID) ON DELETE CASCADE,
    CONSTRAINT vf_rule_fk FOREIGN KEY (RULE_ID) REFERENCES VALIDATION_RULES (RULE_ID) ON DELETE CASCADE
);

-- 1.6 FINDING_REVIEWS Table
CREATE TABLE FINDING_REVIEWS (
    REVIEW_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FINDING_ID NUMBER(19) NOT NULL,
    OLD_STATUS VARCHAR2(20 CHAR),
    NEW_STATUS VARCHAR2(20 CHAR) NOT NULL CHECK (NEW_STATUS IN ('OPEN', 'REVIEWED', 'IGNORED')),
    REVIEW_COMMENT VARCHAR2(4000 CHAR),
    REVIEWED_BY VARCHAR2(200 CHAR) NOT NULL,
    REVIEWED_AT TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    CONSTRAINT fr_finding_fk FOREIGN KEY (FINDING_ID) REFERENCES VALIDATION_FINDINGS (FINDING_ID) ON DELETE CASCADE
);

-- 1.7 AI_ADVISORIES Table
CREATE TABLE AI_ADVISORIES (
    ADVISORY_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RUN_ID NUMBER(19) NOT NULL,
    FINDING_ID NUMBER(19),
    ADVISORY_SCOPE VARCHAR2(30 CHAR) NOT NULL CHECK (ADVISORY_SCOPE IN ('BOM', 'FINDING')),
    AI_STATUS VARCHAR2(30 CHAR) NOT NULL,
    AI_SUMMARY CLOB,
    AI_SUGGESTED_ACTION CLOB,
    AI_PROVIDER VARCHAR2(100 CHAR),
    REQUESTED_BY VARCHAR2(200 CHAR),
    GENERATED_AT TIMESTAMP(6) WITH TIME ZONE,
    ERROR_CODE VARCHAR2(100 CHAR),
    ERROR_MESSAGE VARCHAR2(4000 CHAR),
    CONSTRAINT ai_run_fk FOREIGN KEY (RUN_ID) REFERENCES BOM_RUNS (RUN_ID) ON DELETE CASCADE,
    CONSTRAINT ai_finding_fk FOREIGN KEY (FINDING_ID) REFERENCES VALIDATION_FINDINGS (FINDING_ID) ON DELETE CASCADE
);

-- 1.8 DIAGNOSTIC_LOGS Table
CREATE TABLE DIAGNOSTIC_LOGS (
    LOG_ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CORRELATION_ID VARCHAR2(100 CHAR) NOT NULL,
    RELATED_RUN_ID NUMBER(19),
    RELATED_FINDING_ID NUMBER(19),
    COMPONENT_CODE VARCHAR2(100 CHAR) NOT NULL,
    STAGE VARCHAR2(200 CHAR) NOT NULL,
    SOURCE_MODE VARCHAR2(30 CHAR) CHECK (SOURCE_MODE IN ('LIVE', 'MOCK', 'N/A')),
    STATUS VARCHAR2(30 CHAR) NOT NULL,
    OCCURRED_AT TIMESTAMP(6) WITH TIME ZONE NOT NULL,
    DURATION_MS NUMBER(19) CHECK (DURATION_MS >= 0),
    EVENT_LEVEL VARCHAR2(30 CHAR) NOT NULL CHECK (EVENT_LEVEL IN ('INFO', 'WARNING', 'ERROR')),
    ERROR_CODE VARCHAR2(100 CHAR),
    DETAILS VARCHAR2(4000 CHAR),
    CONSTRAINT dl_run_fk FOREIGN KEY (RELATED_RUN_ID) REFERENCES BOM_RUNS (RUN_ID) ON DELETE CASCADE,
    CONSTRAINT dl_finding_fk FOREIGN KEY (RELATED_FINDING_ID) REFERENCES VALIDATION_FINDINGS (FINDING_ID) ON DELETE CASCADE
);


